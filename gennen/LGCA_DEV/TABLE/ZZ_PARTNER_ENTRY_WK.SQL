/
COMMENT ON COLUMN ZZ_PARTNER_ENTRY_WK.CLASS_CODE IS 'クラスコード'
/
COMMENT ON COLUMN ZZ_PARTNER_ENTRY_WK.SIMEI_NO IS '氏名NO'
/
