CREATE TABLE ZZ_PARTNER_ENTRY_WK
(
    PROC_GRP_NO                    VARCHAR2(40 CHAR) NOT NULL,
    PROC_ORDER                     VARCHAR2(10 CHAR) NOT NULL,
    PROC_TYPE                      CHAR(1 CHAR) NOT NULL,
    PROC_RESULT                    CHAR(1 CHAR),
    KAMOKU_CODE                    VARCHAR2(10 CHAR) NOT NULL,
    CLASS_CODE                     VARCHAR2(18 CHAR) NOT NULL,
    SIMEI_NO                       VARCHAR2(20 CHAR) NOT NULL,
    MOUSIKOMISYA                   VARCHAR2(20 CHAR),
    JYUKOUBI                       CHAR(8 CHAR),
    UPDATE_DATE                    VARCHAR2(14 CHAR),
